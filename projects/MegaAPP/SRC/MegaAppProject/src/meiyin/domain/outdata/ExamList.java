package meiyin.domain.outdata;

import java.util.Date;

public class ExamList {

	private String name;
	private String examAdd;
	private Date examTime;
	private String suitePic;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getExamAdd() {
		return examAdd;
	}
	public void setExamAdd(String examAdd) {
		this.examAdd = examAdd;
	}
	public Date getExamTime() {
		return examTime;
	}
	public void setExamTime(Date examTime) {
		this.examTime = examTime;
	}
	public String getSuitePic() {
		return suitePic;
	}
	public void setSuitePic(String suitePic) {
		this.suitePic = suitePic;
	}
	
	
}
